package com.example.demo;


public class SystemsUn {
	
	String url;
	String date;
	String name;
	
	public SystemsUn(String url, String date, String name) {
		super();
		this.url = url;
		this.date = date;
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
